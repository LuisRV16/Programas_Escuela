package Dudas;
public class Programa07 {
    public static void main(String[] args) {
        //Casteo de tipos
        float x=(float)3.1;//3.1 es considerado como tipo double
        System.out.println("x = "+x);
        float y=9.4f;
        System.out.println("y = "+y);
        double alfa=3.54546363738;
        System.out.println("Valor (double): "+alfa);
        System.out.println("Valor:(float) "+(float)alfa);
        System.out.println("Valor:(int)"+(int)alfa);
        
    }
}
